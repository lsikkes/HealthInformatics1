namespace Visualizer.ViewModels
{
    using System;
    using System.Collections.Generic;
    using System.Linq;
    using System.Text;
    using System.Threading.Tasks;
    using System.Windows.Media;
    using System.Windows.Media.Imaging;
    using Visualizer.Models;
    using Visualizer.Models.Cars;
    using Visualizer.Utilities;

    /// <summary>
    /// NormalCarViewModel Class
    /// </summary>
    /// <seealso cref="Visualizer.ViewModels.AbstractViewModel" />
    /// <autogeneratedoc />
    public class NormalCarViewModel : EmergencyCarViewModel
    {
        #region Constructors

        /// <summary>
        /// Initializes a new instance of the <see cref="NormalCarViewModel"/> class.
        /// </summary>
        /// <param name="pos">The position.</param>
        /// <param name="id">The identifier.</param>
        public NormalCarViewModel(VRPosition pos, int id)
            : base(pos, id)
        {
            this.CarModel = new CarModel(pos, id);
            this.CarImage = new ImageBrush();
            this.GetCarImage();
            this.CarIconImage = new ImageBrush();
            this.GetCarIconImage();
            this.Model = this.CarModel;
            this.Height = this.CarModel.Height;
            this.Width = this.CarModel.Width;
            this.SetPosition(pos);
        }

        #endregion Constructors
    }
}
